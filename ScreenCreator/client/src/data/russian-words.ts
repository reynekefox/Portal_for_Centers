// Helper function to get words by letter count
export function getWordsByLength(length: number): string[] {
    return RUSSIAN_WORDS[length] || [];
}

// Get all words flattened
export function getAllWords(): string[] {
    return Object.values(RUSSIAN_WORDS).flat();
}

// Get random word of specific length
export function getRandomWord(length: number): string {
    const words = getWordsByLength(length);
    if (words.length === 0) return "";
    return words[Math.floor(Math.random() * words.length)];
}
